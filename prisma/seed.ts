async function main() {
  console.log("シードデータを作成中...");

  const password = await bcrypt.hash("seed123456", 12);
  const seedUser = await prisma.user.upsert({
    where: { email: "seed@example.com" },
    update: {},
    create: {
      name: "サッカーコーチ",
      email: "seed@example.com",
      password,
    },
  });

  console.log(`シードユーザー作成: ${seedUser.name}`);

  for (const p of practices) {
    const existing = await prisma.practice.findFirst({ where: { title: p.title } });
    if (!existing) {
      await prisma.practice.create({
        data: { ...p, authorId: seedUser.id },
      });
      console.log(`✓ ${p.title}`);
    } else {
      console.log(`スキップ（既存）: ${p.title}`);
    }
  }

  console.log(`\n完了！${practices.length}件の練習メニューをシードしました。`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
